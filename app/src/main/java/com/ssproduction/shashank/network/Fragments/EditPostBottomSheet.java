public class EditPostBottomSheet extends BottomSheetDialogFragment {

    private RelativeLayout deletePost, clickNewPhoto, addFrame, viewProfile, editProfile;

    private static final int GALLERY_PIC = 1;
    private ProgressDialog dialog;
    private DatabaseReference publishDatabase;
    private StorageReference mImageStorage;
    private String mCurrentUser;

    private BottomSheetBehavior.BottomSheetCallback mBottomSheetBehaviorCallback = new BottomSheetBehavior.BottomSheetCallback() {


        @Override
        public void onStateChanged(@NonNull View bottomSheet, int newState) {
            if (newState == BottomSheetBehavior.STATE_HIDDEN) {
                dismiss();
            }

        }

        @Override
        public void onSlide(@NonNull View bottomSheet, float slideOffset) {
        }
    };

    @SuppressLint("RestrictedApi")
    @Override
    public void setupDialog(Dialog mDialog, int style) {
        super.setupDialog(mDialog, style);
        View contentView = View.inflate(getContext(), R.layout.fragment_edit_post_bottom_sheet, null);
        mDialog.setContentView(contentView);

        dialog = new ProgressDialog(getActivity());
        mImageStorage = FirebaseStorage.getInstance().getReference();
        mCurrentUser = FirebaseAuth.getInstance().getCurrentUser().getUid();
        publishDatabase = FirebaseDatabase.getInstance().getReference().child("Publish");


        CoordinatorLayout.LayoutParams params = (CoordinatorLayout.LayoutParams) ((View) contentView.getParent()).getLayoutParams();
        CoordinatorLayout.Behavior behavior = params.getBehavior();

        if( behavior != null && behavior instanceof BottomSheetBehavior ) {
            ((BottomSheetBehavior) behavior).setBottomSheetCallback(mBottomSheetBehaviorCallback);
        }
    }

}
